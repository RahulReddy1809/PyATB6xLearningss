for i in range(0,10): # 0 to 9 --> 10times
    print(i)
    if i ==5:
        break

# always draw expression result table (ERT)

# / i / condition / o/p /

# /0 / 1==5 -> False / o/p = 0
# /1 / 2==5 -> False / o/p = 1
# /2 / 3==5 -> False / o/p = 2
# /3 / 4==5 -> False / o/p = 3
# /4 / 5==5 -> False / o/p = 4
# /5 / 6==5 -> True / o/p = 5 and then break out of for Loop

# Here, program will print till 5 because first print(i) is updated firstly
# program will execute until the loop i == 5, and then it will break
# remaining numbers will not print


