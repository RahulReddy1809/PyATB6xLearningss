# Task for the day
# Take 3 input from the users
# Perform add, sub, mul, div

num1 = float(input("Enter the numb1 : "))
num2 = float(input("Enter the numb2 : "))
num3 = float(input("Enter the numb3 : "))

add_result = num1 + num2 + num3
sub_result = num1 - num2 - num3
mul_result = num1 * num2 * num3
div_result = (num1/num2) / num3

print(add_result)
print(sub_result)
print(mul_result)
print(div_result)

